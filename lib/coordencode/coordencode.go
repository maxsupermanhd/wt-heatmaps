type SpaceTime struct {
	Time    uint32
	X, Y, Z float64
}

func EncodeSpaceTime(from []SpaceTime) []byte {

	// step 0: convert floating point to fixed point 4 bit precision (multiply by 16, round to nearest integer)
	// step 1: write deltas of those coordinates and time to their own channels as varints
	//			(skipping deltas where all x, y and z are 0) (and keep count of how many samples written)
	// step 2: write one byte reserved for versioning as 0 to output
	// step 3: write number of samples as whatever you want (uint64 is fine)
	// step 4: copy written channels of T, X, Y, Z to the output

	bufs := [4][]byte{}
	numSamples := uint64(0)

	prev := [4]int64{}
	for _, sample := range from {
		rx := int64(math.Round(sample.X*16)) - prev[1]
		ry := int64(math.Round(sample.Y*16)) - prev[2]
		rz := int64(math.Round(sample.Z*16)) - prev[3]
		if rx == 0 && ry == 0 && rz == 0 {
			continue
		}
		rt := sample.Time - uint32(prev[0])
		prev[0] = int64(rt)
		prev[1] = rx
		prev[2] = ry
		prev[3] = rz
		bufs[0] = binary.AppendUvarint(bufs[0], uint64(rt))
		bufs[1] = binary.AppendVarint(bufs[1], rx)
		bufs[2] = binary.AppendVarint(bufs[2], ry)
		bufs[3] = binary.AppendVarint(bufs[3], rz)
		numSamples++
	}

	out := &bytes.Buffer{}
	out.WriteByte(0)
	binary.Write(out, binary.LittleEndian, numSamples)
	out.Write(bufs[0])
	out.Write(bufs[1])
	out.Write(bufs[2])
	out.Write(bufs[3])

	return out.Bytes()
}

func DecodeSpaceTimeStream(from io.Reader) ([]SpaceTime, error) {
	r := &byteReader{r: from}

	// step 1: read version byte and decide what to do
	rVer, err := r.ReadByte()
	if err != nil {
		return nil, fmt.Errorf("reading first byte: %w", err)
	}
	if rVer != 0 {
		return nil, fmt.Errorf("unknown version %d", rVer)
	}

	// step 2: read number of samples
	var rNumSamples uint64
	err = binary.Read(r, binary.LittleEndian, rNumSamples)
	if err != nil {
		return nil, fmt.Errorf("reading num samples: %w", err)
	}

	// TODO: sanity check number of samples

	// step 3: read strides of T, X, Y, Z and convert them back to floating point
	prev := int64(0)
	ret := make([]SpaceTime, rNumSamples)
	for i := range ret {
		val, err := binary.ReadUvarint(r)
		if err != nil {
			return nil, fmt.Errorf("reading series t: %w", err)
		}
		prev += int64(val)
		ret[i].Time = uint32(prev)
	}
	prev = 0
	for i := range ret {
		val, err := binary.ReadVarint(r)
		if err != nil {
			return nil, fmt.Errorf("reading series x: %w", err)
		}
		prev += val
		ret[i].X = float64(prev) / 16
	}
	prev = 0
	for i := range ret {
		val, err := binary.ReadVarint(r)
		if err != nil {
			return nil, fmt.Errorf("reading series y: %w", err)
		}
		prev += val
		ret[i].Y = float64(prev) / 16
	}
	prev = 0
	for i := range ret {
		val, err := binary.ReadVarint(r)
		if err != nil {
			return nil, fmt.Errorf("reading series z: %w", err)
		}
		prev += val
		ret[i].Z = float64(prev) / 16
	}

	return ret, nil
}

type byteReader struct {
	r   io.Reader
	buf [1]byte
}

func (w *byteReader) ReadByte() (byte, error) {
	_, err := io.ReadFull(w.r, w.buf[:])
	if err != nil {
		return 0, err
	}
	return w.buf[0], nil
}

func (w *byteReader) Read(p []byte) (n int, err error) {
	return w.r.Read(p)
}

